    public final <T> void showData(ArrayList<T> list, DefaultTableModel model) {
        model.setRowCount(0);
        for (T t : list) {
            if (t instanceof ThiSinh) {
                ThiSinh thiSinh = (ThiSinh) t;
                model.addRow(new Object[]{thiSinh.getMaSV(), thiSinh.getCaThi(), thiSinh.getLanThi(),
                    thiSinh.getsBD(), thiSinh.getViTri(), thiSinh.getMaPhong()
                });
            }
        }
    }

