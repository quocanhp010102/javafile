    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String maSV = jTextField1.getText();
        String caThi = jTextField2.getText();
        String lanThi = jComboBox1.getSelectedItem().toString();
        String sBD = jTextField3.getText();
        String viTri = jTextField4.getText();
        String maPhong = jTextField5.getText();
        if (jTextField1.getText() == "" || jTextField2.getText() == "" || jTextField3.getText() == ""
                || jTextField4.getText() == "" || jTextField5.getText() == "") {
            JOptionPane.showMessageDialog(rootPane, "khong duoc de trong o nao");
        } else {
            List<ThiSinh> toRemove = new ArrayList<>();

            for (ThiSinh c : dsTS) {
                if (c.getsBD().equals(sBD)) {
                    toRemove.add(c);
                }
            }

            dsTS.removeAll(toRemove);
            ThiSinh b = new ThiSinh();
            b.setMaSV(maSV);
            b.setCaThi(caThi);
            b.setLanThi(Integer.parseInt(lanThi));
            b.setsBD(sBD);
            b.setViTri(viTri);
            b.setMaPhong(maPhong);

            dsTS.add(b);
            showData(dsTS, tblVeModel);
            luuFile(dsTS, fileNameTS);
            System.out.println("sua thanh cong");
            JOptionPane.showMessageDialog(rootPane, "Sua thanh cong!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String sBD = jTextField3.getText();
        int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa không?", "Xóa", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION) {
            // Thực hiện xóa

            Iterator<ThiSinh> iterator = dsTS.iterator();
            while (iterator.hasNext()) {
                ThiSinh r = iterator.next();
                if (r.getsBD().equals(sBD)) {
                    iterator.remove(); // Xóa phần tử hiện tại
                    break; // Thoát khỏi vòng lặp sau khi xóa
                }
            }

            JOptionPane.showMessageDialog(rootPane, "xoa thanh cong!!");

            luuFile(dsTS, fileNameTS);
            showData(dsTS, tblVeModel);
            System.out.println("Đã xóa!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

